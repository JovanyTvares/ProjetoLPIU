    private void btAssociarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAssociarActionPerformed
        associacao = new Associacao();
        associacao.setPlacaVeiculo(txtPlaca.getText());
        associacao.setCnhCondutor(txtCnh.getText());

        try {
            arqoutAssociacao.Adicionar(associacao);
        } catch (IOException ex) {
            Logger.getLogger(Associacoes.class
                    .getName()).log(Level.SEVERE, null, ex);
        }

        JOptionPane.showMessageDialog(
                null, "Associacao inserida com sucesso!");

        limpar();

        int cont = 1;

        List<Associacao> listacar = new ArrayList<Associacao>();

        listacar = arqinAssociacao.getTodasAssociacoes();
        DefaultTableModel modeloAssocicao = (DefaultTableModel) jtAssociacao.getModel();

        for (int i = jtAssociacao.getRowCount() - 1;
                i >= 0; --i) {
            modeloAssocicao.removeRow(i);
        }

        for (int i = 0;
                i < listacar.size();
                i++) {
            modeloAssocicao.addRow(listacar.get(i).getAssociacao());
            cont++;
        }
    }//GEN-LAST:event_btAssociarActionPerformed

