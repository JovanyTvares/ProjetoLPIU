    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jtAssociacao.getModel();
        List<Associacao> list = new ArrayList<Associacao>();
        ArrayList<Associacao> nova_list = new ArrayList<Associacao>();
        linhaAssociacao = jtAssociacao.getSelectedRow();
        Object valor = modelo.getValueAt(linhaAssociacao, 0);
        list = arqinAssociacao.getTodasAssociacoes();

        for (int i = 0; i < list.size(); i++) {
            if (!(list.get(i).getPlacaVeiculo().equals(valor))) {
                nova_list.add(list.get(i));
            }
        }
        try {
            arqoutAssociacao.recadastrar_todas(nova_list);
        } catch (IOException ex) {
            Logger.getLogger(Associacoes.class.getName()).log(Level.SEVERE, null, ex);
        }

        modelo.removeRow(linhaAssociacao);
        JOptionPane.showMessageDialog(null, "Registro apagado com sucesso");

        limpar();

        btAlterar.setEnabled(false);
        btExcluir.setEnabled(false);        // TODO add your handling code here:
    }//GEN-LAST:event_btExcluirActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        int cont = 1;

        // Listar dados das pessoas na tabela Pessoas
        List<Pessoas> listaPessoa = new ArrayList<Pessoas>();

        listaPessoa = arqinPessoa.getTodasPessoas();
        DefaultTableModel modeloPessoa = (DefaultTableModel) jtPessoas.getModel();

        for (int i = jtPessoas.getRowCount() - 1; i >= 0; --i) {
            modeloPessoa.removeRow(i);
        }

        for (int i = 0; i < listaPessoa.size(); i++) {
            modeloPessoa.addRow(listaPessoa.get(i).getPessoa());
            cont++;
        }        // TODO add your handling code here:

        // Listar dados dos veículos na tabela Veículos
        List<Automovel> listaAutomovel = new ArrayList<Automovel>();

        listaAutomovel = arqinVeiculo.getTodosCarros();
        DefaultTableModel modeloAutomovel = (DefaultTableModel) jtVeiculos.getModel();

        for (int i = jtVeiculos.getRowCount() - 1; i >= 0; --i) {
            modeloAutomovel.removeRow(i);
        }

        for (int i = 0; i < listaAutomovel.size(); i++) {
            modeloAutomovel.addRow(listaAutomovel.get(i).getcarro());
            cont++;
        }

        // Listar dados das associações na tabela Associação 
        List<Associacao> listaAssociacao = new ArrayList<Associacao>();

        listaAssociacao = arqinAssociacao.getTodasAssociacoes();
        DefaultTableModel modeloAssociacao = (DefaultTableModel) jtAssociacao.getModel();

        for (int i = jtAssociacao.getRowCount() - 1; i >= 0; --i) {
            modeloAssociacao.removeRow(i);
        }

        for (int i = 0; i < listaAssociacao.size(); i++) {
            modeloAssociacao.addRow(listaAssociacao.get(i).getAssociacao());
            cont++;
        }

    }//GEN-LAST:event_formWindowOpened

