    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        
        ArrayList<Associacao> listAssociacao = new ArrayList<>();
        listAssociacao = (ArrayList<Associacao>) arqinAssociacao.getTodasAssociacoes();
        
        listAssociacao.get(linhaAssociacao).setPlacaVeiculo(txtPlaca.getText());
        listAssociacao.get(linhaAssociacao).setCnhCondutor(txtCnh.getText());

        try{
            arqoutAssociacao.recadastrar_todas(listAssociacao);
        }catch (IOException ex) {
            Logger.getLogger(Associacoes.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(null, "Registro alterado com sucesso!");
        
        limpar();
        
        DefaultTableModel modelo = (DefaultTableModel) jtAssociacao.getModel();
        
        if (jtAssociacao.getRowCount() > 0) {
            for (int i = jtAssociacao.getRowCount() - 1; i>0; i--){
                modelo.removeRow(i);
            }
        }
        
        List<Associacao> listAuto2 = new ArrayList<>();
        listAuto2 = arqinAssociacao.getTodasAssociacoes();
        
        for (int i=0; i< listAuto2.size(); i++){
            modelo.addRow(listAuto2.get(i).getAssociacao());
        }
        
        btAlterar.setEnabled(false);
        btExcluir.setEnabled(false);                                             // TODO add your handling code here:
    }//GEN-LAST:event_btAlterarActionPerformed

